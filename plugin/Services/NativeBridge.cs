        private int OnQueryData(int queryType, IntPtr paramsBuf, uint paramsLen,
            out IntPtr outResultBuf, out uint outResultLen)
        {
            outResultBuf = IntPtr.Zero;
            outResultLen = 0;

            try
            {
                byte[] resultBytes;

                switch (queryType)
                {
                    case QueryPlayerState:
                        resultBytes = SerializePlayerState();
                        break;
                    case QueryTrackInfo:
                        resultBytes = SerializeTrackInfo();
                        break;
                    case QueryCoverData:
                        resultBytes = SerializeCoverData();
                        break;
                    case QueryLyrics:
                        resultBytes = SerializeLyrics();
                        break;
                    case QueryPlaylistList:
                        resultBytes = SerializePlaylistList();
                        break;
                    case QueryNowPlayingList:
                        resultBytes = SerializeNowPlayingList(paramsBuf, paramsLen);
                        break;
                    case QueryRadioStations:
                        resultBytes = SerializeRadioStations(paramsBuf, paramsLen);
                        break;
                    case QueryOutputDevices:
                        resultBytes = SerializeOutputDevices();
                        break;
                    case QueryLibrarySearchGenre:
                        resultBytes = SerializeGenreSearch(paramsBuf, paramsLen);
                        break;
                    case QueryLibrarySearchArtist:
                        resultBytes = SerializeArtistSearch(paramsBuf, paramsLen);
                        break;
                    case QueryLibrarySearchAlbum:
                        resultBytes = SerializeAlbumSearch(paramsBuf, paramsLen);
                        break;
                    case QueryLibrarySearchTitle:
                        resultBytes = SerializeTrackSearch(paramsBuf, paramsLen);
                        break;
                    case QueryLibraryBrowseGenres:
                        resultBytes = SerializeGenreBrowse(paramsBuf, paramsLen);
                        break;
                    case QueryLibraryBrowseArtists:
                        resultBytes = SerializeArtistBrowse(paramsBuf, paramsLen);
                        break;
                    case QueryLibraryBrowseAlbums:
                        resultBytes = SerializeAlbumBrowse(paramsBuf, paramsLen);
                        break;
                    case QueryLibraryBrowseTracks:
                        resultBytes = SerializeTrackBrowse(paramsBuf, paramsLen);
                        break;
                    case QueryLibraryGenreArtists:
                        resultBytes = SerializeGenreArtists(paramsBuf, paramsLen);
                        break;
                    case QueryLibraryArtistAlbums:
                        resultBytes = SerializeArtistAlbums(paramsBuf, paramsLen);
                        break;
                    case QueryLibraryAlbumTracks:
                        resultBytes = SerializeAlbumTracks(paramsBuf, paramsLen);
                        break;
                    case QueryNowPlayingDetails:
                        resultBytes = SerializeNowPlayingDetails();
                        break;
                    case QueryAlbumCover:
                        resultBytes = SerializeAlbumCover(paramsBuf, paramsLen);
                        break;
                    case QueryCoverCacheBuildStatus:
                        resultBytes = SerializeCoverCacheBuildStatus();
                        break;
                    case QueryPlaybackPosition:
                        resultBytes = SerializePlaybackPosition();
                        break;
                    case QueryAlbumCoverBatch:
                        resultBytes = SerializeAlbumCoverBatch(paramsBuf, paramsLen);
                        break;
                    case QueryNowPlayingRating:
                        resultBytes = SerializeNowPlayingRating();
                        break;
                    case QueryNowPlayingLfmRating:
                        resultBytes = SerializeNowPlayingLfmRating();
                        break;
                    case QueryNowPlayingListOrdered:
                        resultBytes = SerializeNowPlayingListOrdered(paramsBuf, paramsLen);
                        break;
                    default:
                        Logger.Warn("Unknown query type: {0}", queryType);
                        return -1;
                }

                outResultBuf = Marshal.AllocHGlobal(resultBytes.Length);
                Marshal.Copy(resultBytes, 0, outResultBuf, resultBytes.Length);
                outResultLen = (uint)resultBytes.Length;

                return 0;
            }
            catch (Exception ex)
            {
                Logger.LogError(ex, "Query callback error for type {0}", queryType);
                return -1;
            }
        }

        private int OnExecuteCommand(int commandType, IntPtr paramsBuf, uint paramsLen,
            out IntPtr outResultBuf, out uint outResultLen)
        {
            outResultBuf = IntPtr.Zero;
            outResultLen = 0;

            try
            {
                switch (commandType)
                {
                    case CmdSetMute:
                        return ApplyBool(paramsBuf, paramsLen, _player.SetMute);
                    case CmdSetShuffle:
                        return ApplyBool(paramsBuf, paramsLen, _player.SetShuffle);
                    case CmdSetScrobble:
                        return ApplyBool(paramsBuf, paramsLen, _player.SetScrobble);
                    case CmdSetAutoDj:
                        return ApplyBool(paramsBuf, paramsLen, _player.SetAutoDj);
                    case CmdSetRepeat:
                        return ApplyRepeat(paramsBuf, paramsLen);
                    case CmdSetRating:
                        return ApplySetRating(paramsBuf, paramsLen);
                    case CmdSetLfmRating:
                        return ApplySetLfmRating(paramsBuf, paramsLen);
                    case CmdOutputSwitch:
                        return ApplyStringSetter(paramsBuf, paramsLen, _player.SetOutputDevice);
                    case CmdPlaylistPlay:
                        return ApplyStringSetter(paramsBuf, paramsLen, _playlist.PlayPlaylist);
                    case CmdLibraryPlayAll:
                        return ApplyBool(paramsBuf, paramsLen, _playlist.PlayAllLibrary);
                    case CmdNowPlayingListPlay:
                        return ApplyStringSetter(paramsBuf, paramsLen, _playlist.PlayNowPlayingTrack);
                    case CmdNowPlayingListMove:
                        return ApplyMove(paramsBuf, paramsLen);
                    case CmdNowPlayingListRemove:
                        return ApplyRemoveIndex(paramsBuf, paramsLen);
                    case CmdNowPlayingListSearch:
                        return ApplyNowPlayingSearch(paramsBuf, paramsLen);
                    case CmdLibraryQueueGenre:
                        return ApplyLibraryQueue(paramsBuf, paramsLen, MetaTag.Genre);
                    case CmdLibraryQueueArtist:
                        return ApplyLibraryQueue(paramsBuf, paramsLen, MetaTag.Artist);
                    case CmdLibraryQueueAlbum:
                        return ApplyLibraryQueue(paramsBuf, paramsLen, MetaTag.Album);
                    case CmdLibraryQueueTrack:
                        return ApplyLibraryQueue(paramsBuf, paramsLen, MetaTag.Title);
                    case CmdNowPlayingQueue:
                        return ApplyNowPlayingQueue(paramsBuf, paramsLen);
                    default:
                        Logger.Warn("Unknown command type: {0}", commandType);
                        return -1;
                }
            }
            catch (Exception ex)
            {
                Logger.LogError(ex, "Command callback error for type {0}", commandType);
                return -1;
            }
        }

        private static byte[] CopyParams(IntPtr paramsBuf, uint paramsLen)
        {
            if (paramsBuf == IntPtr.Zero || paramsLen == 0)
                return new byte[0];
            var buf = new byte[paramsLen];
            Marshal.Copy(paramsBuf, buf, 0, (int)paramsLen);
            return buf;
        }

        private static int ApplyBool(IntPtr paramsBuf, uint paramsLen, Func<bool, bool> setter)
        {
            var dto = MessagePackSerializer.Deserialize<SetBoolParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            return setter(dto.value) ? 0 : 1;
        }

        private int ApplyRepeat(IntPtr paramsBuf, uint paramsLen)
        {
            var dto = MessagePackSerializer.Deserialize<SetRepeatParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            if (!Enum.TryParse<Enumerations.RepeatMode>(dto.mode, out var mode))
            {
                Logger.Warn("Invalid repeat mode '{0}'", dto.mode ?? "<null>");
                return 1;
            }
            return _player.SetRepeatMode(mode) ? 0 : 1;
        }

        private static int ApplyStringSetter(IntPtr paramsBuf, uint paramsLen, Func<string, bool> setter)
        {
            var dto = MessagePackSerializer.Deserialize<StringValueParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            return setter(dto.value ?? string.Empty) ? 0 : 1;
        }

        private int ApplySetRating(IntPtr paramsBuf, uint paramsLen)
        {
            var dto = MessagePackSerializer.Deserialize<StringValueParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            return _track.SetNowPlayingRating(dto.value ?? string.Empty) ? 0 : 1;
        }

        private int ApplySetLfmRating(IntPtr paramsBuf, uint paramsLen)
        {
            var dto = MessagePackSerializer.Deserialize<SetLfmRatingParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            if (!Enum.TryParse<LastfmStatus>(dto.status, out var status))
            {
                Logger.Warn("Invalid lfm status '{0}'", dto.status ?? "<null>");
                return 1;
            }
            return _track.SetNowPlayingLastfmStatus(status) ? 0 : 1;
        }

        private int ApplyMove(IntPtr paramsBuf, uint paramsLen)
        {
            var dto = MessagePackSerializer.Deserialize<MoveParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            if (dto.from < 0 || dto.to < 0)
                return 1;
            return _playlist.MoveNowPlayingTrack(dto.from, dto.to) ? 0 : 1;
        }

        private int ApplyRemoveIndex(IntPtr paramsBuf, uint paramsLen)
        {
            var dto = MessagePackSerializer.Deserialize<IndexParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            if (dto.index < 0)
                return 1;
            return _playlist.RemoveFromNowPlayingList(dto.index) ? 0 : 1;
        }

        private int ApplyNowPlayingSearch(IntPtr paramsBuf, uint paramsLen)
        {
            var dto = MessagePackSerializer.Deserialize<StringValueParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            var query = dto.value ?? string.Empty;
            if (string.IsNullOrWhiteSpace(query))
                return 1;

            var source = SearchSourceHelper.GetSearchSource(_userSettings);
            var match = _track.SearchNowPlayingList(query, source);
            if (string.IsNullOrEmpty(match))
                return 1;
            return _playlist.PlayNowPlayingTrack(match) ? 0 : 1;
        }

        private int ApplyLibraryQueue(IntPtr paramsBuf, uint paramsLen, MetaTag metaTag)
        {
            var dto = MessagePackSerializer.Deserialize<LibraryQueueParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            var queueType = MapQueueType(dto.queue_type);
            var query = dto.query ?? string.Empty;
            var source = SearchSourceHelper.GetSearchSource(_userSettings);

            // Title + PlayNow short-circuits the library lookup: `query` is
            // already a file URL produced by librarysearchtitle.
            var fileUrls = metaTag == MetaTag.Title && queueType == QueueType.PlayNow
                ? new[] { query }
                : _library.GetFileUrlsByMetaTag(metaTag, query, source);

            var playFirst = fileUrls != null && fileUrls.Length > 0 ? fileUrls[0] : null;
            return _playlist.QueueFiles(queueType, fileUrls ?? new string[0], playFirst) ? 0 : 1;
        }

        private int ApplyNowPlayingQueue(IntPtr paramsBuf, uint paramsLen)
        {
            var dto = MessagePackSerializer.Deserialize<NowPlayingQueueParams>(
                CopyParams(paramsBuf, paramsLen), MsgPackOptions);
            var files = dto.files ?? new string[0];
            if (files.Length == 0)
                return 1;
            var queueType = MapQueueType(dto.queue_type ?? string.Empty);
            var play = string.IsNullOrEmpty(dto.play) ? null : dto.play;
            return _playlist.QueueFiles(queueType, files, play) ? 0 : 1;
        }

        private static QueueType MapQueueType(string token)
        {
            // Mirrors core/Utilities/Mapping/QueueTypeMapper (which is
            // internal, so we can't reference it from this assembly).
            switch (token)
            {
                case "next": return QueueType.Next;
                case "last": return QueueType.Last;
                case "now": return QueueType.PlayNow;
                case "add-all": return QueueType.AddAndPlay;
                default: return QueueType.Next;
            }
        }

        private void OnFreeBuffer(IntPtr buf)
        {
            if (buf != IntPtr.Zero)
                Marshal.FreeHGlobal(buf);
        }

        #endregion

        #region Serialization Helpers

        private byte[] SerializePlayerState()
        {
            var dto = new PlayerStateDto
            {
                play_state = _player.GetPlayState().ToString(),
                volume = _player.GetVolume(),
                mute = _player.GetMute(),
                shuffle = _player.GetShuffleState().ToString(),
                repeat = _player.GetRepeatMode().ToString(),
                position = _player.GetPosition(),
                scrobble = _player.GetScrobbleEnabled(),
            };
            return MessagePackSerializer.Serialize(dto, MsgPackOptions);
        }

        private byte[] SerializeTrackInfo()
        {
            var track = _track.GetNowPlayingTrackInfo();
            var dto = new TrackInfoDto
            {
                artist = track.Artist ?? string.Empty,
                title = track.Title ?? string.Empty,
                album = track.Album ?? string.Empty,
                year = track.Year ?? string.Empty,
                path = track.Path ?? string.Empty,
            };
            return MessagePackSerializer.Serialize(dto, MsgPackOptions);
        }

        private byte[] SerializeCoverData()
        {
            // Match legacy `Broadcaster.BroadcastCover` fallback chain:
            // embedded artwork first, then MusicBee's downloaded artwork.
            // Going through CoverService keeps that policy in one place.
            var artwork = _coverService.GetNowPlayingCover() ?? string.Empty;
            return MessagePackSerializer.Serialize(artwork, MsgPackOptions);
        }

        private byte[] SerializeLyrics()
        {
            var lyrics = _track.GetNowPlayingLyrics() ?? string.Empty;
            return MessagePackSerializer.Serialize(lyrics, MsgPackOptions);
        }

