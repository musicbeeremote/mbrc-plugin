        /// <summary>
        ///     True between successful StartNetworking and StopNetworking calls.
        ///     Used by InfoWindow to render the status badge in the settings UI.
        /// </summary>
        public bool IsNetworkingRunning => _networkingRunning;

        /// <summary>
        ///     Probe the Rust server by attempting a TCP connect to the
        ///     configured port on the loopback interface. Returns true when
        ///     a TCP connection is accepted within ~500 ms. Cheap enough to
        ///     run from the InfoWindow's "verify" button.
        /// </summary>
        public bool VerifyConnection()
        {
            if (!_networkingRunning) return false;
            try
            {
                using (var client = new System.Net.Sockets.TcpClient())
                {
                    var task = client.ConnectAsync("127.0.0.1", (int)_userSettings.ListeningPort);
                    return task.Wait(TimeSpan.FromMilliseconds(500)) && client.Connected;
                }
            }
            catch
            {
                return false;
            }
        }

        /// <summary>
        ///     Creates a new NativeBridge instance with data providers for callback handling.
        /// </summary>
        public NativeBridge(
            IPlayerDataProvider player,
            ITrackDataProvider track,
            IPlaylistDataProvider playlist,
            ILibraryDataProvider library,
            IUserSettings userSettings,
            ICoverService coverService)
        {
            _player = player ?? throw new ArgumentNullException(nameof(player));
            _track = track ?? throw new ArgumentNullException(nameof(track));
            _playlist = playlist ?? throw new ArgumentNullException(nameof(playlist));
            _library = library ?? throw new ArgumentNullException(nameof(library));
            _userSettings = userSettings ?? throw new ArgumentNullException(nameof(userSettings));
            _coverService = coverService ?? throw new ArgumentNullException(nameof(coverService));
            PreloadNativeLibrary();
        }

        /// <summary>
        ///     Initialize the Rust core with callbacks and storage path.
        /// </summary>
        public unsafe void Initialize(string storagePath)
        {
            if (_initialized)
            {
                Logger.Warn("NativeBridge already initialized");
                return;
            }

            try
            {
                // Create and pin callback delegates
                _playPauseCallback = OnPlayPause;
                _stopCallback = OnStop;
                _nextCallback = OnNext;
                _previousCallback = OnPrevious;
                _setVolumeCallback = OnSetVolume;
                _setPositionCallback = OnSetPosition;
                _queryDataCallback = OnQueryData;
                _executeCommandCallback = OnExecuteCommand;
                _freeBufferCallback = OnFreeBuffer;

                var callbacks = new MbrcCallbacks
                {
                    player_play_pause = Marshal.GetFunctionPointerForDelegate(_playPauseCallback).ToPointer(),
                    player_stop = Marshal.GetFunctionPointerForDelegate(_stopCallback).ToPointer(),
                    player_next = Marshal.GetFunctionPointerForDelegate(_nextCallback).ToPointer(),
                    player_previous = Marshal.GetFunctionPointerForDelegate(_previousCallback).ToPointer(),
                    player_set_volume = Marshal.GetFunctionPointerForDelegate(_setVolumeCallback).ToPointer(),
                    player_set_position = Marshal.GetFunctionPointerForDelegate(_setPositionCallback).ToPointer(),
                    query_data = Marshal.GetFunctionPointerForDelegate(_queryDataCallback).ToPointer(),
                    execute_command = Marshal.GetFunctionPointerForDelegate(_executeCommandCallback).ToPointer(),
                    free_buffer = Marshal.GetFunctionPointerForDelegate(_freeBufferCallback).ToPointer(),
                };

                var pathBytes = Encoding.UTF8.GetBytes(storagePath + "\0");
                fixed (byte* pathPtr = pathBytes)
                {
                    var result = NativeMethods.mbrc_initialize(callbacks, pathPtr);
                    if (result == 0)
                    {
                        _initialized = true;
                        Logger.Info("Rust core initialized with storage path: {0}", storagePath);
                    }
                    else
                    {
                        Logger.LogError("Rust core initialization failed (error code: {0})", result);
                    }
                }
            }
            catch (DllNotFoundException ex)
            {
                Logger.LogError(ex, "mbrc_core.dll not found — Rust core will not be available");
            }
            catch (Exception ex)
            {
                Logger.LogError(ex, "Failed to initialize Rust core");
            }
        }

        /// <summary>
        ///     Start the Rust HTTP server. The listening port is read from
        ///     core_settings.json in the storage path passed to Initialize;
        ///     C# WinForms is the only writer of that file.
        /// </summary>
        public void StartNetworking()
        {
            if (!_initialized)
            {
                Logger.Warn("Cannot start networking: Rust core not initialized");
                return;
            }

            try
            {
                var result = NativeMethods.mbrc_start_networking();
                if (result == 0)
                {
                    _networkingRunning = true;
                    Logger.Info("Rust HTTP server starting (port from core_settings.json)");
                }
                else
                {
                    Logger.LogError("Failed to start Rust HTTP server (error code: {0})", result);
                }
            }
            catch (Exception ex)
            {
                Logger.LogError(ex, "Failed to start Rust networking");
            }
        }

        /// <summary>
        ///     Stop the Rust HTTP server.
        /// </summary>
        public void StopNetworking()
        {
            if (!_initialized) return;

            try
            {
                var result = NativeMethods.mbrc_stop_networking();
                _networkingRunning = false;
                if (result == 0)
                    Logger.Info("Rust HTTP server stopped");
                else
                    Logger.Warn("Rust HTTP server stop returned {0}", result);
            }
            catch (Exception ex)
            {
                Logger.LogError(ex, "Failed to stop Rust networking");
            }
        }

        /// <summary>
        ///     Forward a MusicBee notification to the Rust core.
        /// </summary>
        public void HandleNotification(int notificationType)
        {
            if (!_initialized) return;

            try
            {
                NativeMethods.mbrc_handle_notification(notificationType);
            }
            catch (Exception ex)
            {
                Logger.LogError(ex, "Failed to forward notification {0} to Rust core", notificationType);
            }
        }

        public void Dispose()
        {
            if (_disposed) return;
            _disposed = true;

            try
            {
                if (_initialized)
                {
                    NativeMethods.mbrc_shutdown();
                    _initialized = false;
                    Logger.Info("Rust core shut down");
                }
            }
            catch (Exception ex)
            {
                Logger.LogError(ex, "Error during Rust core shutdown");
            }

            if (_libraryHandle != IntPtr.Zero)
            {
                FreeLibrary(_libraryHandle);
                _libraryHandle = IntPtr.Zero;
            }

            // Release pinned delegates
            _playPauseCallback = null;
            _stopCallback = null;
            _nextCallback = null;
            _previousCallback = null;
            _setVolumeCallback = null;
            _setPositionCallback = null;
            _queryDataCallback = null;
            _executeCommandCallback = null;
            _freeBufferCallback = null;
        }

