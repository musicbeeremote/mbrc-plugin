        /// <summary>
        ///     Pre-loads mbrc_core.dll from the same directory as the plugin assembly.
        /// </summary>
        private void PreloadNativeLibrary()
        {
            var assemblyDir = Path.GetDirectoryName(
                Assembly.GetExecutingAssembly().Location);

            if (assemblyDir == null)
            {
                Logger.Warn("Could not determine assembly directory for native library preload");
                return;
            }

            var dllPath = Path.Combine(assemblyDir, "mbrc_core.dll");

            if (!File.Exists(dllPath))
            {
                Logger.Warn("mbrc_core.dll not found at {0}", dllPath);
                return;
            }

            _libraryHandle = LoadLibrary(dllPath);

            if (_libraryHandle == IntPtr.Zero)
            {
                var errorCode = Marshal.GetLastWin32Error();
                Logger.LogError("Failed to pre-load mbrc_core.dll (Win32 error: {0})", errorCode);
            }
            else
            {
                Logger.Info("Pre-loaded mbrc_core.dll from {0}", dllPath);
            }
        }
