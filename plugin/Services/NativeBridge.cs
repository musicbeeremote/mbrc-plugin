        private delegate int CommandCallbackDelegate(
            int commandType,
            IntPtr paramsBuf,
            uint paramsLen,
            out IntPtr outResultBuf,
            out uint outResultLen);

        [UnmanagedFunctionPointer(CallingConvention.Cdecl)]
        private delegate void FreeBufferDelegate(IntPtr buf);

        #endregion

        #region Native Library Preload (kernel32)

